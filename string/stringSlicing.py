str = "Welcome To Python"

# str[0:]= start with index 0 and prints whole string
print(str[0:])  

# str[0:7]= start with index 0 and prints upto index 6
print(str[0:7])

# str[0::2]= start with index 0 and prints whole string with increment by 2
print(str[0::2])

# str[0:10:2]= start with index 0 and prints upto index 9 with increment by 2
print(str[0:10:2])

# str[-1::-1]=print reverse string
print(str[-1::-1])

# str[-1:-7:-1]=print reverse string upto index -6
print(str[-1:-7:-1])

# str[-1::-2]=print reverse string with decrement by 2
print(str[-1::-2])
