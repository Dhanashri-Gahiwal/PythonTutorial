'''
 Arithmetic operators
addition(+)
subtraction(-)
multiplication(*)
division(/) : It returns the answer in float
modulus(%)
exponent(**)
floor division(//) : It returns the answer in integer
'''
print("addition:",(5+3))
print("subtraction:",(5-3))
print("multiplication:",(5*3))
print("division:",(4/3))
print("floor division:",(4//3))
print("division:",(4/2))
print("modulus:",(4%3))
print("exponent:",(4**2)) # 4*4 = 16
print("exponent:",(4**3)) # 4*4*4 = 64
