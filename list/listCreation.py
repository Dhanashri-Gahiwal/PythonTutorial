# create a list
l = [1,2,3,4]
print(l)

# get specific element in list
print(l[1])

# create a mixed list
l1 = [1,2.3,'a','b']
print(l1)

# get specific element in mixed list
print(l1[0])
print(l1[2])

# create nested list
l2 = [1,2,3.4,4.5,"b","d",["anu","sonu"],[10,12]]
print(l2)

# get specific element in nested list
print(l2[0])
print(l2[2])
print(l2[4])
print(l2[6])
print(l2[6][0])
print(l2[7][1])